    void checar(String nome) {
        try {
            //"SELECT*FROM Cliente WHERE nome='"+nome+"';";
                con = Conexao.obterConexao();
                stmt = con.createStatement();
                rs = stmt.executeQuery("SELECT*FROM Cliente WHERE nome='"+nome+"'");
            if(rs.next()){
               String pesq = rs.getString("id");
                setId(pesq);
            }
        } catch (SQLException ex) {
            Logger.getLogger(VerClient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

    private void btnAttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttActionPerformed
        if(NomeCl.getText().isEmpty() && EmailCl.getText().isEmpty() && BairoCl.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Nao e possivel atualizar, por vafor verifique os campos os campos!");
        }else{
            try {
                con=Conexao.obterConexao();
                ps = con.prepareStatement("UPDATE Cliente SET nome = '"+NomeCl.getText()+"', Email ='"+EmailCl.getText()+"', Bairro ='"+BairoCl.getText()+"'WHERE id = '"+getId()+"'");
                int rowsUpdated = ps.executeUpdate();
                if(rowsUpdated>0){
                    JOptionPane.showMessageDialog(null,"Dados atualizados com sucesso!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(VerClient.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAttActionPerformed


    private void delFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delFActionPerformed
        int tabelaSeleciona = JAgua.getSelectedRow();
        if (tabelaSeleciona >= 0) {
            try {
                Object chave = JAgua.getValueAt(tabelaSeleciona, 0);
                ps = con.prepareStatement("DELETE FROM FUNCIONARIO WHERE ID='" + chave + "'");
                int apagar = ps.executeUpdate();
                if (apagar > 0) {
                    JOptionPane.showMessageDialog(null, "Produto apagado com sucesso!");
                    try {
                        con = Conexao.obterConexao();
                        stmt = con.createStatement();
                        rs = stmt.executeQuery("SELECT*FROM FUNCIONARIO");
                        JAgua.setModel(DbUtils.resultSetToTableModel(rs));
                    } catch (SQLException ex) {
                        Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Falha ao apagar o funcionario");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_delFActionPerformed


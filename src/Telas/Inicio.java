    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        DefaultTableModel model = (DefaultTableModel) caixa.getModel();
        String codigo = psPd.getText();
        String nome = jNome.getText();
        int preco = Integer.parseInt(Jpreco.getText());
        String litro = Jlitro.getText();
        if (!codigo.isEmpty() && !nome.isEmpty() && !Jpreco.getText().isEmpty() && !litro.isEmpty()) {
        model.addRow(new Object[]{codigo, nome, litro, preco});
        somaTotal += preco;
        total.setText(String.valueOf(somaTotal));
        model.fireTableDataChanged();
        }else{
            JOptionPane.showMessageDialog(null, "Os campos devem estar preechidos");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        DefaultTableModel model = (DefaultTableModel) caixa.getModel();
        int rowIndex = caixa.getSelectedRow(); // Obtém o índice da linha selecionada
        if (rowIndex != -1) { // Verifica se uma linha está selecionada
            int precoRemovido = (int) model.getValueAt(rowIndex, 3);
            somaTotal -= precoRemovido;
            model.removeRow(rowIndex);
            total.setText(String.valueOf(somaTotal));
        } else {
            JOptionPane.showMessageDialog(null, "Escolha o produto a mover");
        }
    }//GEN-LAST:event_jButton11ActionPerformed

